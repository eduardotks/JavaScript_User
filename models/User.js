class User
{
    //parâmetros do método construtor
    constructor(name, gender, birth, country, email, password, photo, admin)
    {
        this.name = name;  //this."""atributo""" = parâmetro
        this.gender = gender;
        this.birth = birth;
        this.country = country;
        this.email = email;
        this.password = password;
        this.photo = photo;
        this.admin = admin;
    }
}